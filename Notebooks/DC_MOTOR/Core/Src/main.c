void posControlPID(){
	e2 = p_target - count1;
	ei2 += e2*0.001;
	ed2 = (e2-pre_e2)/0.001;
	if (ei2> 1000){
		ei2  =1000;
	}else if(ei2<-1000){
		ei2 = -1000;
	}
	u2 = e2*kp2+ei2*ki2+ed2*kd2;
	if (u2>0)dir=-1;
	else if(u2<0)dir = 1;
	else dir = 0;
	pwm = abs(u2);
	if(pwm>200){pwm = 200;}
	else if ((pwm<50)&&(e2!=0)){
		pwm = 50;

	}
}

void calculatePIDSpeed(){
	e1 = v_target - v1; // tinh toan loi ty le
	ei1 += e1 * 0.001; // tinh toan loi tich phan
	ed1 = (e1 - pre1) / 0.001;
	u1 = kp1 * e1 + ki1 * ei1; //Tinh tong bo dieu khien
	pre1 = e1;
	if(u1 < 0) dir = 1; //bien doi chiue vong quay
	else dir = -1;
	pwm = abs(u1);

	if(pwm > 1000) pwm = 1000;//Bao hoa xung cap
}

